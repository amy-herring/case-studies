###Diversity & Inclusiveness:

The Duke University Community Commitment states: ``Because diversity is essential to fulfilling the university’s mission, Duke is committed to building an inclusive and diverse university community.  Every student, faculty, and staff member —whatever their race, gender, age, ethnicity, cultural heritage or nationality; religious or political beliefs; sexual orientation or gender identity; or socioeconomic, veteran or ability status—has the right to inclusion, respect, agency and voice in the Duke community.  Further, all members of the University community have a responsibility to uphold these values and actively foster full participation in university life.''

It is my goal for our learning environment to facilitate learning and intellectual development for everyone. To help accomplish this:
  
  - If you have a name and/or set of pronouns that differ from those that appear in your official Duke records, please let me know!
  - We are constantly learning more about diverse perspectives and identities. If something was said in class (by anyone) that made you feel uncomfortable, please talk to me about it. 

### Academic integrity:

Duke University is a community dedicated to scholarship, leadership, and service and to the principles of honesty, fairness, respect, and accountability. Citizens of this community commit to reflect upon and uphold these principles in all academic and nonacademic endeavors, and to protect and promote a culture of integrity.

Remember the Duke Community Standard that you have agreed to abide by:
  
  > To uphold the Duke Community Standard:
  
  > - I will not lie, cheat, or steal in my academic endeavors;
> - I will conduct myself honorably in all my endeavors; and
> - I will act if the Standard is compromised.

Cheating on exams or plagiarism on homework assignments, lying about an illness or absence and other forms of academic dishonesty are a breach of trust with classmates and faculty, violate the <a href="http://www.studentaffairs.duke.edu/conduct/resources/dcs">Duke Community Standard</a>, and will not be tolerated. Such incidences will result in a 0 grade for all parties involved.  Additionally, there may be penalties to your final class grade along with being reported to the <a href="http://www.studentaffairs.duke.edu/conduct/about/ucbhearings">Undergraduate Conduct Board</a>.

Please review the Academic Dishonesty policies <a href="https://studentaffairs.duke.edu/conduct">here</a>. 

- Much of the work assigned in class is collaborative. The individual case study must be completed independently. In addition, regular assessments must be completed individually before they are tackled by the group. 

**Referencing code:** The web contains an enormous volume of code that you may find useful. You are welcome to make use of any online resources (e.g. StackOverflow), but you must explicitly cite your sources. Any recycled code that is discovered and is not explicitly cited will be treated as plagiarism. On the individual case study you may not directly share code with another student in this class, and on team assignments you may not share code with another team in this class (you are welcome to discuss the problems together and ask for advice, but you may not send or make use of code from another team). 

### Course components:

#### Class sessions:

If you miss class or would like to review the material covered in class, you can view the recordings [here](https://trinity.capture.duke.edu/Panopto/Pages/Sessions/List.aspx?folderID=9161c49e-b3d5-4d40-a282-ebe8a938055d). AMY UPDATE Note that you will need to log in with your Net ID.

#### Teams:

On the first day of class, we will develop teams of 5-7 students - these teams will stay consistent 
throughout the semester (barring extraordinary circumstances). You will work in these teams during 
class and on the five group case studies. The process of forming teams will be completely transparent 
and based on self-administered personal skills assessments.

#### Final Project:

You will be responsible for the completion of an open ended final project for this course, 
the goal of which is to tackle an "interesting" problem using the tools and techniques covered 
in this class. Additional details on the project will be provided as the course progresses.

Each team's work will also be shared with and evaluated by at least one other team at an 
earlier stage in order to provide feedback in the form of code review. 

#### Application exercises:

Short exercises will frequently be administered at the begininning of class to assess progress and identify areas for further development. Students will first complete these individually and hand them in; subsequently, the group will discuss the exercise and turn in a group solution. 

Students who miss individual and group submissions (due to late arrival or absence) will receive 0 points for those submissions. However, only the top 80% of submissions (separately for individual and group submissions) will count towards a student's point total.

#### Homework:

Beyond the in class activities, you will be assigned larger data analysis tasks throughout the semester. These assignments will be completed individually. 

Homework with the lowest score for each student will be dropped.

#### Case Studies:

The objective of the  is to give you hands on experience with data analysis using modern statistical software. 

All team members are expected to contribute equally to the completion of each assignment and you will be asked to evaluate your team members after a few assignments are due.

Students are expected to make use of their team's git repository on the course's github page as their central collaborative platform. Commits to this repository will be used as a metric of each team member's relative contribution for each lab.

Lab with the lowest score for each student will be dropped.

#### Exams:

There will be a two take home midterms that you are expected to complete individually. Each exam will ask you to complete a number of small programming and or analysis tasks related to the material presented in the class. The exams will be written to take between 2-5 hours. The exact structure and content of the exams will be discussed in more detail before they are assigned.



#### Interactive tutorials:

These are self-paced interactive tutorials that will be assigned intermittently throughout the semester. They will be graded on a check/no check basis (though you'll receive feedback on the way as you complete them) and they will count towards extra credit.

